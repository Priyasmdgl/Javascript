let a=5, b=5;
//Arithmetic operators
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
a++;
console.log(a);
b--;
console.log(b);
//Assignment operators
a=10
b=20;
console.log(a+=b); //a=a+b
console.log(a-=b); //a=a-b
console.log(a*=b); //a=a*b
console.log(a/=b); //a=a/b
console.log(a%=b); //a=a%b
//Relational operators
//returns a boolean value
let x=15, y=20;
console.log(x>y);
console.log(x<y);
console.log(x<=y);
console.log(x>=y);
console.log(x!=y);
console.log(x==y);
console.log(x===y);
//ternary operator
console.log(x<y?x:y);
//logical operators
let m=true, n=false;
console.log(m&&n); //returns true if both variables are true
console.log(m||n); //returns true if one of them is true
console.log(!n); //returns the opposite value
